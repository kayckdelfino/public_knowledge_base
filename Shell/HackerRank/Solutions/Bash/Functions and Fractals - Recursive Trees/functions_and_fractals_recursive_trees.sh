# Input Format
# A single integer, N.
# 
# Constraints
# N <= 5
# 
# Output Format
# The Nth iteration of the Fractal Tree, as shown above. It should be a matrix of 63 rows and 100 columns. (i.e. 6300 printable characters) It should be composed entirely of underscores and ones, in a manner similar to the examples provided. Do not include any extra leading or trailing spaces. 


declare -A grid

for r in {1..64}
do
    for c in {1..100}
    do
        grid["$r,$c"]="_"
    done
done

grid["64,50"]="1"

function printgrid {
    for r in {1..63}
    do
        for c in {1..100}
        do
            echo -n ${grid["$r,$c"]}
        done
        echo
    done
}

function tree {
    line=$1
    size=$2
    base=$(($line + 1))

    for ((i=0; i<$size; i++))
    do
        trunkcur=$(($line - $i))
        branchcur=$(($line - $size - $i))
        for c in {1..100}
        do
            if test "1" = ${grid["$base,$c"]-_}
            then
                grid["$trunkcur,$c"]="1"
            fi

            right=$(($c + $i + 1))
            if test "1" = ${grid["$base,$right"]-_}
            then
                grid["$branchcur,$c"]="1"
            fi

            left=$(($c - $i - 1))
            if test "1" = ${grid["$base,$left"]-_}
            then
                grid["$branchcur,$c"]="1"
            fi
        done
    done
}

function frac {
    iter=$1
    start=$2
    size=$3
    
    if [ $iter -eq 0 ]
    then
        return
    fi

    tree $start $size

    frac $((iter - 1)) $((start - size * 2)) $((size / 2))
}

read n

frac $n 63 16

printgrid