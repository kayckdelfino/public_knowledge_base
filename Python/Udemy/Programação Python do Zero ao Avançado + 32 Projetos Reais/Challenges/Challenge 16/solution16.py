# For this challenge, I want you to ask the user to enter a number. If the number is greater than 10, print "Number is greater than 10". Otherwise, print "Number is less than or equal to 10".


number = int(input("Enter a number: "))

if number > 10:
    print("The number is greater than 10")
else:
    print("The number is less than or equal to 10")